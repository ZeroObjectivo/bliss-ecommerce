<script>
document.addEventListener('DOMContentLoaded', () => {
    // Accordion functionality
    const triggers = document.querySelectorAll('.accordion-trigger');
    
    triggers.forEach(trigger => {
        trigger.addEventListener('click', () => {
            const item = trigger.closest('.accordion-item'); // Use closest to find the parent item
            const content = trigger.nextElementSibling; // The content is right after the trigger
            const isOpen = item.classList.contains('active');
            
            // Close other items in the same container
            const container = item.parentNode;
            container.querySelectorAll('.accordion-item').forEach(otherItem => {
                if (otherItem !== item) {
                    otherItem.classList.remove('active');
                    otherItem.querySelector('.accordion-content').style.maxHeight = null;
                }
            });
            
            // Toggle current item
            if (isOpen) {
                item.classList.remove('active');
                content.style.maxHeight = null;
            } else {
                item.classList.add('active');
                content.style.maxHeight = content.scrollHeight + "px";
            }
        });
    });

    // Form submission for contact form
    document.getElementById('shipping-contact-form')?.addEventListener('submit', function(e) {
        e.preventDefault();
        const form = this;
        const formData = new FormData(form);
        
        fetch(form.action, {
            method: 'POST',
            headers: { 'X-Requested-With': 'XMLHttpRequest' }, // Important for AJAX requests
            body: formData
        })
        .then(res => res.json())
        .then(data => {
            if (data.success) {
                form.reset();
                showDynamicAlert('Your concern has been successfully submitted to our support team.');
            } else {
                // Display specific error from server if available, otherwise a generic message
                const errorMessage = data.error || 'Failed to send message. Please try again.';
                alert(errorMessage);
            }
        })
        .catch(err => {
            console.error('Error submitting form:', err);
            alert('An unexpected error occurred. Please try again.');
        });
    });
});

// Function to close success alert
function closeSuccessAlert() {
    const alert = document.getElementById('success-alert');
    if(alert) {
        alert.style.opacity = '0';
        alert.style.transform = 'translateX(-50%) translateY(-20px)';
        alert.style.transition = 'all 0.4s cubic-bezier(0.4, 0, 0.2, 1)';
        setTimeout(() => alert.remove(), 400);
    }
}

// Auto-close success alert after 3 seconds and remove query param
setTimeout(() => {
    closeSuccessAlert();
    const url = new URL(window.location);
    url.searchParams.delete('success');
    window.history.replaceState({}, document.title, url);
}, 3000);

// Function to show dynamic success alert
function showDynamicAlert(message) {
    const alertHtml = `
        <div class="alert alert-success-proper" id="success-alert">
            <div class="alert-icon-circle">
                <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3"><polyline points="20 6 9 17 4 12"></polyline></svg>
            </div>
            <span class="alert-text">${message}</span>
            <button class="alert-close-btn" onclick="closeSuccessAlert()">
                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3" stroke-linecap="round" stroke-linejoin="round"><line x1="18" y1="6" x2="6" y2="18"></line><line x1="6" y1="6" x2="18" y2="18"></line></svg>
            </button>
            <div class="alert-progress-bar"></div>
        </div>
    `;
    const div = document.createElement('div');
    div.innerHTML = alertHtml.trim();
    document.body.appendChild(div.firstChild);
    
    setTimeout(() => {
        closeSuccessAlert();
    }, 3000);
}
</script>